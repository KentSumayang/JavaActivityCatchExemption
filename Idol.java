public class Idol<VariableType> {
    
    private String idolName;
    private String idolOrigin;

    public void setIdolName(String idolName){
        this.idolName=idolName;
    }
    public String getIdolName(){
        return idolName;
    }
    public void setIdolOrigin(String idolOrigin){
        this.idolOrigin=idolOrigin;
    }
    public String getIdolOrigin(){
        return idolOrigin;
    }
}
